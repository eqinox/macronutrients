import { Element, ElementId } from "@/lib/types";

function aminoAcid(
    name: string,
    description: string,
    bullets: string[],
): Element {
    return {
        name,
        description,
        properties: [
            {
                action: "Роля в тялото",
                itemsAboutAction: bullets.map((text) => ({
                    startingPhrase: text,
                    description: text,
                })),
            },
        ],
    };
}

